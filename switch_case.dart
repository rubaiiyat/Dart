void main(){
    int mark=110;

    switch(mark){
        case >100:
            print("Invalid mark");
            break;
        case >=80:
            print("A+");
            break;
        case >=70:
            print("A");
            break;
        case >=60:
            print("B");
            break;
        case >=50:
            print("C");
            break;
        default:
            print("Invalid mark");
    }
}