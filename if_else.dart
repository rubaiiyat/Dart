void main(){
    int mark=32;

    if(mark>=80){
        print("A+");
    }else if(mark>=70){
        print("A");
    }else if(mark>=60){
        print("A-");    
    }else if(mark>=50){
        print("B");
    }else if(mark>=40){
        print("C");
    }else if(mark>=33){
        print("D"); 
    }else{
        print("F"); 
    }
    
}